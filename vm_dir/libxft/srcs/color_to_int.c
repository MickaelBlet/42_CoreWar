#include "libxft.h"

int		libx_color_to_int(t_libx_color color)
{
	return ((((int)color.r & 0xFF) << 0x10) +
			(((int)color.g & 0xFF) << 0x08) +
			(((int)color.b & 0xFF) << 0x00));
}
